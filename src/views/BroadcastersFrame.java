    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String id = this.broadcasterIdInput.getText();
        if(!id.isEmpty() && this.controller.selectBroadcaster(id)) {
           this.showBroadcasterForm();
        } else {
           this.showInvalidIdError();
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void showBroadcasterForm() {
        BroadcasterForm frame = new BroadcasterForm(controller, this);
        frame.setVisible(true);
        this.setVisible(false);
    }
    
